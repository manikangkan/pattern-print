void main()
{
    int i, j, k = 0, n = 11, m = 1;
    for (i = 1; i <= 21; i++)
    {
        i <= n ? k++ : k--;
        m = k - 1;
        for (j = 1; j <= 21; j++)
        {
            if (j >= n + 1 - k && j <= n - 1 + k)
                printf(" %d", j < 11 ? m++ % 10 : m-- % 10);
            else
                printf(" ");
        }
        printf("\n");
    }
}
